void OV3640GetAeLut(P_AE_LUT_INFO_STRUCT pFeatureParaIn,
                         P_IMAGE_SENSOR_AE_LUT_STRUCT pFeatureParaOut,
                         kal_uint32 FeatureParaOutLen, kal_uint32 *pRealFeatureParaOutLen)
{                                                                                        							
   register const device_info_struct *pAeInfoLut = NULL;						
	register kal_uint32 Mode = pFeatureParaIn->DscMode;
	register kal_uint32 idxHz = 0;/* default 50 Hz */								
	/* check banding freq 60 Hz */													
	if (CAM_BANDING_60HZ == pFeatureParaIn->Band)
	{																				
		idxHz = 1;																	
	}																				
	/* check valid scene mode id */												
	if (Mode < OV3640_MAX_SCENE_MODE_NO)
	{																				
	#ifdef OV3640_MAX_VIDEO_MODE_NO	
		if (KAL_TRUE==pFeatureParaIn->VideoMode)
{
                        kal_uint8 i, night = 0;
                        kal_int8 diff, min_diff = 127;

            if (Mode == CAM_VIDEO_NIGHT)
                night = 1;
			
                        for (i = 0; i < OV3640_MAX_VIDEO_MODE_NO; i++)
{                                                                                        
                        		if (OV3640_AE_VIDEO_FRAME_RATES[i][1] == night)
{                                                                                        
                	diff = OV3640_AE_VIDEO_FRAME_RATES[i][0]-pFeatureParaIn->VideoFrameRate;
                        			if (diff >= 0 && diff < min_diff)
                           		{
                	    Mode = OV3640_AE_VIDEO_FRAME_RATES[i][2];
                              			min_diff = diff;
            }
        }
    }                                                                
  }                                                                                      
       #endif
		*(pFeatureParaOut->pAeLut) = (exposure_lut_struct *) OV3640_AE_LUTs[Mode][idxHz];
		pAeInfoLut = OV3640_device_support_info_list[Mode];
}                                                                                        
	ASSERT(pFeatureParaOut->pAeLut != NULL);
	ASSERT(pAeInfoLut != NULL);													
	kal_mem_cpy(pFeatureParaOut->pDeviceInfo, pAeInfoLut, sizeof(device_info_struct));
} /* OV3640GetAeLut() */
                                                                                         
   
#endif //ISP_SUPPORT                                                                             
